package programming;

public class Patterns {
	public static void main(String[] args) {
		//* * * * *
		//* * * * *
		//* * * * *
		//* * * * *
		//* * * * *
		
		int n=5;
		for (int i = 1; i <=5; i++)  // -> rows
		
		{
			for (int j = 1; j <=n; j++)  // -> columns
			{
				
				System.out.print("* ");
			}
			System.out.println();
			
		}
	}

}



